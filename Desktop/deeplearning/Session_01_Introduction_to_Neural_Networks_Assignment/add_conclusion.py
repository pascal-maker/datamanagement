        "3. **Regularization**\n",
        "   - Apply Batch Normalization for deep networks\n",
        "   - Use Dropout (0.2-0.5) to prevent overfitting\n",
        "   - Early stopping based on validation loss\n",
        "   - L2 regularization if needed\n",
        "\n",
        "4. **Optimization**\n",
        "   - Use Adam optimizer (learning_rate=0.001) as default\n",
        "   - He initialization with ReLU\n",
        "   - Appropriate batch size (32-256)\n",
        "   - Monitor training curves\n",
        "\n",
        "5. **Hyperparameter Tuning**\n",
        "   - Learning rate: Most important hyperparameter\n",
        "   - Number of layers and neurons\n",
        "   - Dropout rate\n",
        "   - Batch size\n",
        "   - Use validation set to guide decisions\n",
        "\n",
        "### **Common Pitfalls to Avoid**\n",
        "\n",
        "❌ **Not normalizing input data** → Slow convergence, poor performance\n",
        "❌ **Too high learning rate** → Unstable training, divergence\n",
        "❌ **Too much dropout (>0.5)** → Underfitting, poor training accuracy\n",
        "❌ **No validation set** → Can't detect overfitting\n",
        "❌ **Wrong activation in output layer** → Poor predictions\n",
        "❌ **Ignoring class imbalance** → Biased predictions\n",
        "❌ **Too complex architecture** → Overfitting, slow training\n",
        "❌ **Not using early stopping** → Overfitting to training data\n",
        "\n",
        "### **Signs of Problems**\n",
        "\n",
        "**Overfitting:**\n",
        "- Training accuracy high, validation accuracy low\n",
        "- Large gap between train and validation loss\n",
        "- **Solution**: More dropout, batch normalization, early stopping, more data\n",
        "\n",
        "**Underfitting:**\n",
        "- Both training and validation accuracy low\n",
        "- Loss not decreasing\n",
        "- **Solution**: Larger network, less dropout, more epochs, check learning rate\n",
        "\n",
        "**Vanishing Gradients:**\n",
        "- Very deep network not training\n",
        "- Loss plateaus early\n",
        "- **Solution**: Batch normalization, ReLU activation, residual connections\n",
        "\n",
        "**Exploding Gradients:**\n",
        "- Loss becomes NaN\n",
        "- Weights grow very large\n",
        "- **Solution**: Lower learning rate, gradient clipping, proper initialization\n",
