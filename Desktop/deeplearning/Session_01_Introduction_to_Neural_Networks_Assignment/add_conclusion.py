        "\n",
        "### **Learning Rate (SGD)**\n",
        "- **Too low (0.001)**: Slow convergence, requires many epochs\n",
        "- **Optimal (0.01-0.3)**: Best balance between speed and stability\n",
        "- **Too high (1.0)**: Overshoots optimal weights, unstable training\n",
        "- **Conclusion**: Learning rate is critical for convergence speed and stability\n",
        "\n",
        "### **Optimizers**\n",
        "- **SGD**: Simple but requires careful tuning, slower convergence\n",
        "- **SGD + Momentum**: Faster than vanilla SGD, smoother updates\n",
        "- **Adam**: Best default choice - adaptive learning rates, fast and stable\n",
        "- **RMSprop**: Good for recurrent networks and non-stationary objectives\n",
        "- **Adagrad**: Adapts per-parameter learning rates, good for sparse data\n",
        "- **Nadam**: Adam + Nesterov momentum, often slightly better than Adam\n",
        "- **Conclusion**: Adam/Nadam generally outperform SGD for most tasks\n",
        "\n",
        "### **Dropout Rate**\n",
        "- **0.0**: No regularization, risk of overfitting\n",
        "- **0.2-0.5**: Sweet spot - prevents overfitting while maintaining capacity\n",
        "- **0.8**: EXTREMELY HIGH - severe underfitting, poor training accuracy\n",
        "  - Only 20% of neurons active during training\n",
        "  - Network capacity severely limited\n",
        "  - Large gap between train and validation accuracy\n",
        "- **Conclusion**: Use moderate dropout (0.2-0.5); 0.8 is too aggressive\n",
        "\n",
        "### **Network Architecture**\n",
        "- **Wider networks** (more neurons): Better capacity but risk of overfitting\n",
        "- **Deeper networks** (more layers): Can learn complex patterns\n",
        "- **Too deep**: Vanishing gradients, harder to train without proper techniques\n",
        "- **Pyramid structure** (e.g., 80→40→20): Often works well, gradually reduces dimensions\n",
        "- **Conclusion**: Balance between model complexity and generalization; deeper isn't always better\n",
        "\n",
        "### **Batch Normalization**\n",
        "- Normalizes inputs to each layer, reducing internal covariate shift\n",
        "- **Benefits**: Faster training, allows higher learning rates, acts as regularization\n",
        "- Especially useful for deep networks and when training is unstable\n",
        "- **Conclusion**: BN significantly improves training stability and convergence speed\n",
        "\n",
        "### **Activation Functions**\n",
        "- **ReLU**: Most common, fast, but can have 'dying ReLU' problem\n",
        "- **Tanh**: Zero-centered, but saturates (vanishing gradients)\n",
        "- **Sigmoid**: Outputs [0,1], saturates easily, rarely used in hidden layers\n",
        "- **ELU**: Smooth, helps with vanishing gradients\n",
        "- **SELU**: Self-normalizing properties\n",
        "- **Swish/GELU**: Modern activations, often outperform ReLU\n",
        "- **Conclusion**: ReLU is a solid default; modern activations (ELU, Swish) can improve performance\n",
        "\n",
        "### **Weight Initializers**\n",
        "- **Glorot (Xavier)**: Good for tanh/sigmoid, maintains variance across layers\n",
        "- **He**: Designed for ReLU activations, accounts for ReLU's non-linearity\n",
        "- **LeCun**: Good for SELU activations\n",
        "- **Random**: Poor choice, leads to vanishing/exploding gradients\n",
        "- **Conclusion**: Match initializer to activation - He with ReLU, Glorot with tanh\n",
        "\n",
        "### **Best Practices from Experiments**\n",
        "1. Use **Adam optimizer** with learning rate 0.001 as default\n",
        "2. Apply **Batch Normalization** for deep networks\n",
        "3. Use **moderate dropout** (0.2-0.3) to prevent overfitting\n",
        "4. Choose **He initialization** with ReLU activations\n",
        "5. Start with **2-3 hidden layers** and tune based on performance\n",
        "6. Monitor both **training and validation** metrics to detect overfitting\n",
        "\n",
